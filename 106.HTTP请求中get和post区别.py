"""
1.GET请求是通过URL直接请求数据，数据信息可以在URL中直接看到，比如浏览器访问
    POST请求是放在请求头中的，无法直接看到
2.GET提交的数据有大小限制，一般不超过1024个字节，er这种说法也不完全准确，
    HTTP协议并没有设定URL字节长度的上限，而是浏览器做了些处理，所以长度依据浏览器的不同有所不同，
    POST请求在HTTP协议中没哟说明，一般来说是没有设置限度的，但是实际上浏览器也有默认值。
    总体来说，少量的数据使用GET，大量的数据使用POST
3.GET请求因为数据参数是暴露在URL中的，所以安全性比较低，比如密码是不能暴露的，就不能有GET请求，
    POST请求中，请求参数信息是放在请求头的，所以安全性较高，可以使用。在实际中，涉及登录操作的时候，
    尽量使用HTTPS请求，安全性更好
"""